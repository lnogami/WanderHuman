  Container sexDeterminerButton(BuildContext context) {
    double buttonWidth = MyDimensionAdapter.getWidth(context) * 0.23;
    double buttonHeight = MyDimensionAdapter.getHeight(context) * 0.063;
    return Container(
      // color: Colors.purpleAccent,
      width: MyDimensionAdapter.getWidth(context) * 0.465,
      // height: MyDimensionAdapter.getHeight(context) * 0.2,
      // margin: EdgeInsets.only(right: 18, top: 2),
      // alignment: Alignment.center,
      child: Column(
        children: [
          MyTextFormatter.p(text: "Sex", fontsize: kDefaultFontSize - 2),
          Row(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              GestureDetector(
                onTap: () {
                  setState(() {
                    sexValue = "male";
                  });
                },
                child: Container(
                  width: buttonWidth,
                  height: buttonHeight,
                  decoration: BoxDecoration(
                    color: (sexValue == "male")
                        ? Colors.white60
                        : Colors.transparent,
                    border: BoxBorder.all(
                      color: (sexValue == "male")
                          ? MyColorPalette.borderColor
                          : MyColorPalette.borderColor,
                      width: (sexValue == "male") ? 1.7 : 1,
                    ),
                    borderRadius: BorderRadius.only(
                      topLeft: Radius.circular(10),
                      bottomLeft: Radius.circular(10),
                    ),
                  ),
                  child: Center(child: Text("MALE")),
                ),
              ),

              GestureDetector(
                onTap: () {
                  setState(() {
                    sexValue = "female";
                  });
                },
                child: Container(
                  width: buttonWidth,
                  height: buttonHeight,
                  decoration: BoxDecoration(
                    color: (sexValue == "female")
                        ? Colors.white60
                        : Colors.transparent,
                    border: BoxBorder.all(
                      color: (sexValue == "female")
                          ? MyColorPalette.borderColor
                          : MyColorPalette.borderColor,
                      width: (sexValue == "female") ? 1.7 : 1,
                    ),
                    borderRadius: BorderRadius.only(
                      topRight: Radius.circular(10),
                      bottomRight: Radius.circular(10),
                    ),
                  ),
                  child: Center(child: Text("FEMALE")),
                ),
              ),
            ],
          ),
        ],
      ),
    );
  }

