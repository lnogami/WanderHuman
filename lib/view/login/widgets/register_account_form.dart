  // FOR SIGN UP
  Future<bool> createUserWithEmailAndPassword() async {
    try {
      // ignore: avoid_print
      print("In THE PROCESS OF SIGNING UPPPPPPPPPPPPPPPPPPP");
      // validates if the password and comfirmPassword contains the same value
      if (widget.password.trim() == widget.password.trim()) {
        final userCredential = await FirebaseAuth.instance
            .createUserWithEmailAndPassword(
              email: emailController.text.trim(),
              password: widget.password.trim(),
            );
        // ignore: avoid_print
        print(" ✅✅✅ SIGNED UP: $userCredential");

        // to wait for the account authentication to finish first
        String userID = userCredential.user!.uid;
        toSetAndLoadNewUserCredentials(userID: userID);
      }
      return true;
    } on FirebaseAuthException catch (e) {
      showMyAnimatedSnackBar(
        context: context,
        dataToDisplay: "Email already in use.",
      );
      print("CREATE ACCOUNT ERROR: ${e.message}");
      return false;
    }
  }

