    return Container(
      width: 1,
      height: double.infinity,
      color: const Color.fromARGB(255, 123, 184, 234),
    );
  }

  Container userTypeGroupedRadioButton() {
    return Container(
      // color: Colors.amber, // for debugging purposes only
